#5- ATIVIDADE: Construa um programa para que o usuário possa digitar três valores 
# e o programa irá:​

# A. Multiplicar os dois primeiros valores;​

# B. Dividir o resultado pelo terceiro valor."

X = int(input("Digite aqui o valor de X: ")) # imputando valor de X...
Y = int(input("Digite aqui o valor de Y: ")) # imputando valor de Y...
Z =int(input("Digite aqui o valor de Z: ")) # imputando valor de Z...

MULTIPLICACAO = X * Y # X *(vezes) Y atribui valor a MULTIPLICACAO
print(" O resultado da multiplicacao é:", MULTIPLICACAO)

RESULTADO = MULTIPLICACAO / Z # MULTIPLICACAO /(dividido) por Z atribui valor a RESULTADO
print("O resultado final é: ", RESULTADO)