#Demonstrações de uso de operadores aritméticos...
X = int(input("Digite o valor de X: "))
Y = int(input("Digite o valor de Y: "))

SOMAR = X + Y
print("O resultado da soma:", SOMAR)

SUBTRAIR = X - Y
print("O resultado da subtracao: ", SUBTRAIR)

MULTIPLICAR = X * Y 
print("O resultado da multiplicaçao: ", MULTIPLICAR)

DIVIDIR = X / Y
print("O resultado da divisao: ", DIVIDIR)
