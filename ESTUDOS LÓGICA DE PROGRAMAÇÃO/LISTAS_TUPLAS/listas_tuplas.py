# Demonstração de listas e tuplas em Python

# Para declarar as listas...

LISTA = []
LISTA_INTEIROS = [1, 2, 3, 4, 5]
LISTA_CARACTERES = ['maça', 'banana', 'cereja']
LISTA_MISTA = [1, 'olá', 3.14, True]

# Para declarar as tuplas...
TUPLA_SEMANA = ("segunda", "terça", "quarta")
TUPLA_FIBONACCI = (1 , 2, 3, 5, 8, 13, 21)

#Para acessar os elementos da lista...
print(LISTA_INTEIROS)  # Imprime a lista de inteiros
print(LISTA_CARACTERES[0]) # Imprime o primeiro elemento da lista de caracteres
print(LISTA_MISTA[1]) # Imprime o segundo elemento da lista mista
print(TUPLA_SEMANA[2]) # Imprime o terceiro elemento da tupla de dias da semana
print(TUPLA_FIBONACCI)  # Imprime a tupla de Fibonacci