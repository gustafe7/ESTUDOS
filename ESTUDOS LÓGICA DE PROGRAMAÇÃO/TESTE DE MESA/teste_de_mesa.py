# Demonstração de teste de mesa para o programa da atividade 1

A = int(input("Digite aqui o valor de A: ")) # imputando valor de A...
B = int(input("Digite aqui o valor de B: ")) # imputando valor de B...      
C = int(input("Digite aqui o valor de C: ")) # imputando valor de C...
TOTAL = A + B + C # A + B + C atribui valor a TOTAL
MEDIA = TOTAL / 3 # TOTAL / 3 atribui valor a MEDIA
print("A soma dos valores é:", TOTAL) 
print("A média dos valores é:", MEDIA) 

